getLearner = function(algo) {

  lrn = makeImputeWrapper(
    learner = algo,
    classes = list(numeric = imputeMedian(), factor = imputeMode())
  )
  
  new.lrn = makeRemoveConstantFeaturesWrapper(learner = lrn)
  return(new.lrn)
}

